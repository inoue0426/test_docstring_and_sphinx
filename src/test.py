def print_sum(x, y):
    """print the sum of two numbers
    This function takes two numbers and prints the sum of them.
    The addition is done using the built-in function '+'.

    Parameters:
    -----------
    x: the first number
    y: the second number

    Examples:
    ---------
    >>> print_sum(1, 2)
    3
    >>> print_sum(-1, 2)
    1
    """
    print(x + y)
