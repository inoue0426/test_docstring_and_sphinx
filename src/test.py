def print_sum(x, y):
    """print the sum of two numbers
    This function takes two numbers and prints the sum of them.
    The addition is done using the built-in function '+'.

    Parameters:
    -----------
    x: the first number
    y: the second number

    Examples:
    ---------
    >>> print_sum(1, 2)
    3
    >>> print_sum(-1, 2)
    1
    """
    print(x + y)


def print_multiply(x, y):
    """print the product of two numbers
    This function takes two numbers and prints the product of them.
    The multiplication is done using the built-in function '*'.

    Parameters:
    -----------
    x: the first number
    y: the second number

    Examples:
    ---------
    >>> print_multiply(1, 2)
    2
    >>> print_multiply(-1, 2)
    -2
    """
    print(x * y)
